<b style=" color:#800040;">AIM</b><br />
The overall aim of National Service Scheme, is to give an extension 
dimension  to  the  higher  education  system  and  orient  the  student  youth  to  community  service  
while  they  are  studying  in  educational  i
nstitution.The educated  youth  who  are  expected  to  take  the  reins  of  administration  in  
future are found to be unaware of the problems of the village/slum community and in certain 
cases are indifferent towards their needs and problems. Therefore it is necessary to arouse 
the 
social conscience of the students, and to provide them an opportunity to work with the people 
in  the  villages  and  slums.  It  is  felt  that  their  interaction  with  the  common  villagers  and  slum  
dwellers  will  expose  them  to  the  realities  of  life  and  bring  a
bout  a  change  in  their  social  
perception. 				

<br /><br />
<b style=" color:#800040;">OBJECTIVE</b><br />				
The broad objectives of NSS are to:<br />
(i)
understand the community in which they work<br />
(ii)
understand themselves in relation to their community;<br />
(iii)
identify  the  needs  and  problems  of  the  community  and  involve  
them  in  problem  
solving process;<br />
(iv)
develop among themselves a sense of social and civic responsibility;<br />
(v)
utilize  their  knowledge  in  finding  practical  solution  to  individual  and  community  
problems;<br />
(vi)
develop competence required for group living and sharing of re
sponsibilities;<br />
(vii)
gain skills in mobilizing community participation;<br />
(viii)
acquire leadership qualities and democratic attitude;<br />
(ix)
develop capacity to meet emergencies and natural disasters and<br />
(x)
practice national integration and social harmony.					
