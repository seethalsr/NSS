<td><b>NSS SYMBOL</b></td>
<td></td></tr>
<tr><td></td>
<td>The symbol of the National Service Scheme, as appearing on the cover page of this Manual is based on the 'Rath' wheel of the Konark Sun Temple situated in Orissa. These giant wheels of the Sun Temple portray the cycle of creation, preservation and release, and signify the movement in life across time and space. The design of the symbol, a simplified form of the Sun-chariot wheel primarily depicts movement. The wheel signifies the progressive cycle of life. It stands for continuity as well as change and implies the continuous striving of NSS for social transformation and upliftment.</td>
