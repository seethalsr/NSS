<td><b>MOTTO OF NSS</b></td>
<td></td></tr>
<tr><td></td>
<td>The motto or watchword of the National Service Scheme is : <b>'NOT ME BUT YOU'</b>. This reflects the essence of democratic living and upholds the need for selfless service and appreciation of the other person's point of view and also to show consideration for fellow human beings. It underlines that the welfare of an individual is ultimately dependent on the welfare of society on the whole. Therefore, it should be the aim of the NSS to demonstrate this motto in its day-to-day programme.</td>
