<b style=" color:#800040;">AIM</b><br />
The overall aim of National Service Scheme, is the development of the personality of students through community service.It provides an extention dimension to the higher Education system and orient the student youth to community service.

<br /><br />
<b style=" color:#800040;">OBJECTIVE</b><br />				
The broad objectives of NSS are to:<br />
(i)
understand the community in which they work<br />
(ii)
understand themselves in relation to their community;<br />
(iii)
identify  the  needs  and  problems  of  the  community  and  involve  
them  in  problem  
solving process;<br />
(iv)
develop among themselves a sense of social and civic responsibility;<br />
(v)
utilize  their  knowledge  in  finding  practical  solution  to  individual  and  community  
problems;<br />
(vi)
develop competence required for group living and sharing of re
sponsibilities;<br />
(vii)
gain skills in mobilizing community participation;<br />
(viii)
acquire leadership qualities and democratic attitude;<br />
(ix)
develop capacity to meet emergencies and natural disasters and<br />
(x)
practice national integration and social harmony.					
