Special Camping forms an integral part of National Service Scheme. It has special appeal to the youth as it provides unique opportunities to the students for group living, collective experience sharing and constant interaction with community. 
<br /><br />
Special campings are organised generally on various developmental issues of national importance. In the past the themes of the Special Camping Programmes have been 'Youth Against Famine', 'Youth Against Dirt and Disease', 'Youth for Rural Reconstruction', 'Youth for Eco-Development' and 'Youth for Mass Literacy', 'Youth for National Integration &Social Harmony'			
<br /><br />
<b>Objectives of the Special Camping programme </b><br />
 
 The primary objectives of the special camping programmes are:- <br />
 
(i) making education more relevant to the present situation to meet the felt needs of the communities and supplement the education of
 university/college/school students by bringing them face to face with the community situation. <br />
  (ii) To provide opportunities to students to play their due roles in the implementation of various development programmes
   by planning and executing development projects, which not only help in creating durable community assets in rural areas
    and slums but also result in improvement of the condition of weaker sections of the communities. <br />
    (iii) Encouraging the students and non-students youth to work along with the adults in rural areas, thereby developing their character, 
    social consciousness and commitment, discipline ad healthy and helpful attitudes towards the community;<br />
     (iv) Building up potential youth leaders by exploring the latent potential among the campers, both students as well as local youth (rural and urban), 
     with a view to involve them more intimately in development projects for longer periods. 
     The local leadership generated during the camps would also be useful in ensuring proper maintenance of the assets created as a result of the camps. <br />
     (v) Emphasizing the dignity of labour and self-help and the need for combining physical work with intellectual pursuits, and <br />
      (vi) Encouraging youth to participate enthusiastically in the process of national development, and promote national integration through
       democratic living and cooperative action.
